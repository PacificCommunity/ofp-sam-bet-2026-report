figure_curation_target_type <- function(x) {
  value <- tolower(clean_text(x))
  value <- gsub("[^a-z0-9]+", "-", value)
  value[value %in% c("", "catalog", "catalog-key", "row")] <- "key"
  value[value %in% c("filename", "path", "image")] <- "file"
