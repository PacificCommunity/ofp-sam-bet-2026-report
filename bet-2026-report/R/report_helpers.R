polish_report_caption <- function(x) {
  x <- render_report_text(x)
  x <- gsub("\\s+", " ", x)
  replacements <- c(
    "generated from the current mfclshiny app state" = "prepared from the current assessment plotting state",
    "Generated from the current mfclshiny app state" = "Prepared from the current assessment plotting state",
    "generated by mfclshiny for report use and is the same figure bundle that can be opened in the mfclshiny Report Figures tab" = "prepared for report use",
    "model outputs" = "assessment results",
    "selected model scenarios" = "selected assessment models",
    "selected scenarios" = "selected assessment models",
    "model scenarios" = "assessment models",
    "plotted outputs" = "plotted quantities"
  )
  for (from in names(replacements)) {
    x <- gsub(from, replacements[[from]], x, fixed = TRUE)
  }
  x <- gsub(
    "\\s+(for|in|from|of|used in) the [0-9]{4}\\s+bigeye tuna\\s*(\\(BET\\))?\\s+assessment\\b",
    "",
    x,
    ignore.case = TRUE,
    perl = TRUE
  )
  x <- gsub(
    "^the [0-9]{4}\\s+bigeye tuna\\s*(\\(BET\\))?\\s+assessment\\s+",
    "",
    x,
    ignore.case = TRUE,
    perl = TRUE
  )
  x <- gsub(
    "\\bthe [0-9]{4}\\s+bigeye tuna\\s*(\\(BET\\))?\\s+assessment\\b",
    "",
    x,
    ignore.case = TRUE,
    perl = TRUE
  )
  x <- gsub("\\b(for|in) the ([0-9]{4} [^.,;]+?) assessment results\\b", "\\1 the \\2 assessment", x, perl = TRUE)
  x <- gsub("\\bthe ([0-9]{4} [^.,;]+?) assessment results\\b", "the \\1 assessment", x, perl = TRUE)
  x <- gsub("\\s+([.,;:])", "\\1", x, perl = TRUE)
  x <- trimws(gsub("\\s+", " ", x))
  ifelse(nzchar(x), paste0(toupper(substr(x, 1, 1)), substr(x, 2, nchar(x))), x)
}

latex_escape <- function(x) {
  x <- as.character(x %||% "")
  x <- gsub("\\\\", "\\\\textbackslash{}", x)
  x <- gsub("([#$%&_{}])", "\\\\\\1", x)
  x
}

html_escape <- function(x) {
  x <- as.character(x %||% "")
  x <- gsub("&", "&amp;", x, fixed = TRUE)
  x <- gsub("<", "&lt;", x, fixed = TRUE)
  x <- gsub(">", "&gt;", x, fixed = TRUE)
  x <- gsub("\"", "&quot;", x, fixed = TRUE)
  x <- gsub("'", "&#39;", x, fixed = TRUE)
  x
}

