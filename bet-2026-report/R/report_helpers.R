normalize_figure_placement <- function(x, default = "main") {
  value <- tolower(trimws(as.character(x %||% "")))
  if (!nzchar(value) || is.na(value)) {
    value <- default
  }
  value <- gsub("[^a-z0-9]+", "-", value)
  aliases <- c(
    body = "main",
    report = "main",
    primary = "main",
    include = "main",
    yes = "main",
    y = "main",
    true = "main",
    "1" = "main",
    supplemental = "appendix",
    supplement = "appendix",
    supp = "appendix",
    extra = "appendix",
    excluded = "exclude",
    skip = "exclude",
    omit = "exclude",
    no = "exclude",
    n = "exclude",
    false = "exclude",
    "0" = "exclude"
  )
  if (value %in% names(aliases)) {
    value <- aliases[[value]]
  }
  if (value %in% c("main", "appendix", "exclude")) value else default
}

