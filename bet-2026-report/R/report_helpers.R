`%||%` <- function(x, y) if (is.null(x) || length(x) == 0) y else x

read_report_csv <- function(path) {
  if (!file.exists(path)) {
    return(data.frame(stringsAsFactors = FALSE))
  }
  utils::read.csv(path, stringsAsFactors = FALSE, check.names = FALSE)
}

bind_report_rows <- function(rows) {
  rows <- rows[vapply(rows, function(x) is.data.frame(x) && nrow(x), logical(1))]
  if (!length(rows)) {
    return(data.frame(stringsAsFactors = FALSE))
  }
  cols <- unique(unlist(lapply(rows, names), use.names = FALSE))
  rows <- lapply(rows, function(x) {
    missing <- setdiff(cols, names(x))
    for (name in missing) x[[name]] <- NA
    x[, cols, drop = FALSE]
  })
  do.call(rbind, rows)
}

is_internal_report_table <- function(path) {
  base <- tolower(basename(path))
  grepl(
    paste(
      "^(payload-index(-[0-9]+)?|model-index|plot-summary|report-files|",
      "report-input-.*|report-prep-summary|figure-index|table-index|",
      "generated-table-index|figure-optimization|mfclshiny-.*|",
      ".*build-log.*|.*report-summary)[.]csv$",
      sep = ""
    ),
    base
  )
}

split_catalog_paths <- function(x) {
  x <- as.character(x %||% "")
  x <- unlist(strsplit(x, ";", fixed = TRUE), use.names = FALSE)
  x <- trimws(x)
  x[nzchar(x)]
}

slug_id <- function(x) {
  x <- tolower(as.character(x %||% "item"))
  x <- gsub("[^a-z0-9]+", "-", x)
  x <- gsub("(^-+|-+$)", "", x)
  if (!nzchar(x)) "item" else x
}

clean_text <- function(x) {
  x <- trimws(as.character(x %||% ""))
  x[is.na(x)] <- ""
  x
}

has_text <- function(x) {
  any(nzchar(clean_text(x)))
}

file_match_tokens <- function(path) {
  path <- as.character(path %||% "")
  base <- tolower(basename(path))
  stem <- tolower(tools::file_path_sans_ext(base))
  unique(c(base, stem, slug_id(stem)))
}

is_optimized_sidecar_figure <- function(path) {
  path <- as.character(path %||% "")
  ext <- tolower(tools::file_ext(path))
  sidecar <- ext %in% c("webp", "jpg", "jpeg")
  base_png <- sub("[.](webp|jpe?g)$", ".png", path, ignore.case = TRUE)
  sidecar & file.exists(base_png)
}

preferred_figure_file <- function(file) {
  file <- as.character(file %||% "")
  if (!nzchar(file) || !file.exists(file)) {
    return(file)
  }
  stem <- sub("[.][^.]+$", "", file)
  if (requireNamespace("knitr", quietly = TRUE) &&
      isTRUE(knitr::is_html_output()) &&
      isTRUE(get0("use_webp_figures", ifnotfound = TRUE))) {
    candidate <- paste0(stem, ".webp")
    if (file.exists(candidate)) {
      return(candidate)
    }
  }
  if (requireNamespace("knitr", quietly = TRUE) &&
      isTRUE(knitr::is_latex_output()) &&
      isTRUE(get0("use_pdf_jpeg_figures", ifnotfound = TRUE))) {
    for (ext in c(".jpg", ".jpeg")) {
      candidate <- paste0(stem, ext)
      if (file.exists(candidate)) {
        return(candidate)
      }
    }
  }
  file
}

